def create_letter(
      letter: str, 
      scans: TGeometryList):
      '''
      Generate a list of lines from a DXF file representing a letter made only from lines
      Meant to be pasted into the LETTERS_NUMBERS dictionary as an entry

      Args:
          letter (str): filename
          scans (TGeometryList): letter geometries from importing the dxf file
      '''

      # Print Dictionary key
      print("'" + letter+ "':(")

      # Print each line
      for value in scans:
            
            # LWPolyline exception
            if value[0][0:-2] == 'LWPOLYLINE':
                  print("\t('" + value[0] + "',[")
                  for index in range(len(value[1])-1):
                        print('\t\t' + str(value[1][index]) + ',')
                  print('\t]),')
            else:
                  # Line entry
                  print('\t' + str(value) + ',')

      # End entry
      print("),")
#end def


def create_alphabet():
      '''
      Print out entire NUMBERS_LETTERS dictionary from the Letters folder
      '''
    
      # Get a list of all files in Letters folder
      list = os.listdir('Letters')

      # Generate letter entry
      for letter in list:
            letter_path = 'Letters/' + letter
            letter = letter.rstrip('.dxf')
            create_letter(letter,importer.import_dxf_file(letter_path)) 
      #end for
#end def

if __name__ == "__main__":
      create_alphabet()
